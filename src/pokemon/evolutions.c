u16 pokemon_get_basis_stage(u16 species) {
    u16 i, j;
    for (i = 0; i < POKEMON_CNT; i++) {
        for (j = 0; j < 5; j++) {
            if (pokemon_evolutions[i][j].target == species)
                return pokemon_get_basis_stage(i);
        }
    }
    return species;
}
