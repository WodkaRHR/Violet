.include "overworld_script.s"
.include "levelscript_types.s"
.include "vars.s"
.include "flags.s"
.include "ordinals.s"

.global map_ardeal_rooms_levelscripts
.global map_ardeal_rooms_unlock_tower

map_ardeal_rooms_levelscripts:
    .byte LEVELSCRIPT_TYPE_ON_LOAD 
    .word levelscript_on_load
    .byte 0

levelscript_on_load:
    checkflag FLAG_ARDEAL_DUNGEON_UNLOCKED
    callif NOT_EQUAL dungeon_not_unlocked
    checkflag FLAG_ARDEAL_TOWER_UNLOCKED
    callif EQUAL map_ardeal_rooms_unlock_tower
    end

dungeon_not_unlocked:
    setmaptile 0xE 0x6 0x3DB 1
    setmaptile 0xF 0x6 0x3DB 1
    setmaptile 0x10 0x6 0x3DB 1
    setmaptile 0x11 0x6 0x3DB 1
    setmaptile 0xE 0x7 0x3DB 1
    setmaptile 0xF 0x7 0x3DB 1
    setmaptile 0x10 0x7 0x3DB 1
    setmaptile 0x11 0x7 0x3DB 1
    setmaptile 0xE 0x8 0x3DB 1
    setmaptile 0xF 0x8 0x3DB 1
    setmaptile 0x10 0x8 0x3DB 1
    setmaptile 0x11 0x8 0x3DB 1
    setmaptile 0xE 0x9 0x3E3 0
    setmaptile 0xF 0x9 0x3E3 0
    setmaptile 0x10 0x9 0x3E3 0
    setmaptile 0x11 0x9 0x3E3 0
    return

map_ardeal_rooms_unlock_tower:
    setmaptile 0x17 0x1B 0x284 0
    setmaptile 0x18 0x1B 0x284 0
    setmaptile 0x19 0x1B 0x284 0
    setmaptile 0x1A 0x1B 0x284 0
    setmaptile 0x17 0x1C 0x284 0
    setmaptile 0x18 0x1C 0x284 0
    setmaptile 0x19 0x1C 0x284 0
    setmaptile 0x1A 0x1C 0x284 0
    setmaptile 0x17 0x1D 0x3E1 0
    setmaptile 0x18 0x1D 0x3E1 0
    setmaptile 0x19 0x1D 0x3E1 0
    setmaptile 0x1A 0x1D 0x3E1 0
return